// -----------------------------------------------
// ОСНОВНОЙ ОБЪЕКТ — то что приходит с бэкенда
// -----------------------------------------------
export interface Entity {
  _id: string; // всегда есть, MongoDB добавляет
  title: string; // пример строкового поля
  amount: number; // пример числового поля
  isActive: boolean; // пример булевого поля
  type: 'typeA' | 'typeB'; // пример поля с выбором
  tags: string[]; // пример массива
}

// -----------------------------------------------
// СОЗДАНИЕ — что отправляем на бэк при создании
// Убираем _id (его ещё нет) и поля которые бэк добавляет сам
// -----------------------------------------------
export interface CreateEntity {
  title: string;
  amount: number;
  isActive: boolean;
  type: 'typeA' | 'typeB';
  tags: string[];
}

// -----------------------------------------------
// ОБНОВЛЕНИЕ — что отправляем при редактировании
// Все поля опциональные (?) — отправляем только то что меняем
// -----------------------------------------------
export interface UpdateEntity {
  title?: string;
  amount?: number;
  isActive?: boolean;
  type?: 'typeA' | 'typeB';
  tags?: string[];
}
